       end do
       t0=t-int(t/(2.0d0*pi))*2.0d0*pi
       f=.5d0-(a*cos(t0)+g*sin(t0))/px
    endif

    if (x < 0.0d0) f=-f
  end function f_fress



  recursive function f_expi(x) result(f)
    real(fdp), intent(in) :: x
    real(fdp) :: f

    real(fdp) :: r
    integer :: k, m

    if (x < -1.0d0) then
       m=20-int(80.0/x)
       r=0.0d0
       do k=m,1,-1
          r=k/(1.0d0+dble(k)/(r-x))
       end do
       f=-exp(x)*1.0d0/(r-x)
    else if (x < 0.0d0) then
       f=1.0d0
       r=1.0d0
       do k=1,25
          r=r*dble(k)*x/(dble(k)+1.0d0)**2
          f=f+r
          if (abs(r) <= abs(f)*eps) exit
       end do
       f=0.5772156649015328d0+log(-x)+x*f
    else if (x == 0.0d0) then
       f = -inf
    else if (x <= 40.0d0) then
       f=1.0d0
       r=1.0d0
       do k=1,100
          r=r*dble(k)*x/(dble(k)+1.0d0)**2
          f=f+r
          if (abs(r/f) <= eps) exit
       end do
       f=0.5772156649015328d0+log(x)+x*f
    else
       f=1.0d0
       r=1.0d0
       do k=1,20
          r=r*dble(k)/x
          f=f+r
       end do
       f=exp(x)/x*f
    endif
  end function f_expi



  recursive function f_sini(x) result(f)
    real(fdp), intent(in) :: x
    real(fdp) :: f

    real(fdp), dimension(101) :: bj
    real(fdp) :: xr, xa1, xa0, xa, xgl, xs, xcs, xss, xg, xg1, xg2, xf
    integer :: k, m

    if (x == 0.0d0) then
       f=0.0d0
    else if (x <= 16.0d0) then
       xr=x
       f=x
       do k=1,40
          xr=-.5d0*xr*(2*k-1)/k/(4*k*k+4*k+1)*x**2
          f=f+xr
          if (abs(xr) < abs(f)*eps) return
       end do
    else if (x <= 32.0d0) then
       m=int(47.2+.82*x)
       xa1=0.0d0
       xa0=1.0d-100
       do k=m,1,-1
          xa=4.0d0*k*xa0/x-xa1
          bj(k)=xa
          xa1=xa0
          xa0=xa
       end do
       xs=bj(1)
       do k=3,m,2
          xs=xs+2.0d0*bj(k)
       end do
       bj(1)=bj(1)/xs
       do k=2,m
          bj(k)=bj(k)/xs
       end do
       xr=1.0d0
       xg1=bj(1)
       do k=2,m
          xr=.25d0*xr*(2.0*k-3.0)**2/((k-1.0)*(2.0*k-1.0)**2)*x
          xg1=xg1+bj(k)*xr
       end do
       xr=1.0d0
       xg2=bj(1)
       do k=2,m
          xr=.25d0*xr*(2.0*k-5.0)**2/((k-1.0)*(2.0*k-3.0)**2)*x
          xg2=xg2+bj(k)*xr
       end do
       xcs=cos(x/2.0d0)
       xss=sin(x/2.0d0)
       f=x*xcs*xg1+2*xss*xg2-sin(x)
    else
       xr=1.0d0
       xf=1.0d0
       do k=1,9
          xr=-2.0d0*xr*k*(2*k-1)/x**2
          xf=xf+xr
       end do
       xr=1.0d0/x
       xg=xr
       do k=1,8
          xr=-2.0d0*xr*(2*k+1)*k/x**2
          xg=xg+xr
       end do
       f=pi2-xf*cos(x)/x-xg*sin(x)/x
    endif
  end function f_sini



  recursive function f_cosi(x) result(f)
    real(fdp), intent(in) :: x
    real(fdp) :: f

    real(fdp), dimension(101) :: bj
    real(fdp) :: xr, xa1, xa0, xa, xgl, xs, xcs, xss, xg, xg1, xg2, xf
    integer :: k, m

    if (x == 0.0d0) then
       f = -inf
    else if (x <= 16.0d0) then
       xr=-.25d0*x**2
       f=el+log(x)+xr
       do k=2,40
          xr=-.5d0*xr*(k-1)/(k*k*(2*k-1))*x**2
          f=f+xr
          if (abs(xr) < abs(f)*eps) exit
       end do
    else if (x <= 32.0d0) then
       m=int(47.2+.82*x)
       xa1=0.0d0
       xa0=1.0d-100
       do k=m,1,-1
          xa=4.0d0*k*xa0/x-xa1
          bj(k)=xa
          xa1=xa0
          xa0=xa
       end do
       xs=bj(1)
       do k=3,m,2
          xs=xs+2.0d0*bj(k)
       end do
       bj(1)=bj(1)/xs
       do k=2,m
          bj(k)=bj(k)/xs
       end do
       xr=1.0d0
       xg1=bj(1)
       do k=2,m
          xr=.25d0*xr*(2.0*k-3.0)**2/((k-1.0)*(2.0*k-1.0)**2)*x
          xg1=xg1+bj(k)*xr
       end do
       xr=1.0d0
       xg2=bj(1)
       do k=2,m
          xr=.25d0*xr*(2.0*k-5.0)**2/((k-1.0)*(2.0*k-3.0)**2)*x
          xg2=xg2+bj(k)*xr
       end do
       xcs=cos(x/2.0d0)
       xss=sin(x/2.0d0)
       f=el+log(x)-x*xss*xg1+2*xcs*xg2-2*xcs*xcs
    else
       xr=1.0d0
       xf=1.0d0
       do k=1,9
          xr=-2.0d0*xr*k*(2*k-1)/x**2
          xf=xf+xr
       end do
       xr=1.0d0/x
       xg=xr
       do k=1,8
          xr=-2.0d0*xr*(2*k+1)*k/x**2
          xg=xg+xr
       end do
       f=xf*sin(x)/x-xg*cos(x)/x
    endif
  end function f_cosi



