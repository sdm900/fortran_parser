  recursive function f_ielle(x, phi) result(f)
    real(fdp), intent(in) :: x, phi
    real(fdp) :: f

    real(fdp) :: g, a, a0, b, b0, c, r, d, d0, ce, ck, fac
    integer :: n

    if (phi == 0.0d0) then
       f = 0.0d0
    else if (x == 1.0d0 .and. phi == pi2) then
       f = 1.0d0
    else if (x == 1.0d0) then
       f = sin(phi)
    else if (x == 0.0d0) then
       f = phi
    else
       g=0.0d0
       a0=1.0d0
       b0=sqrt(1.0d0-x*x)
       d0 = phi
       r=x*x
       fac=1.0d0

       do n=1,40
          a=(a0+b0)/2.0d0
          b=sqrt(a0*b0)
          c=(a0-b0)/2.0d0
          fac=2.0d0*fac
          r=r+fac*c*c
          if (phi /= pi2) then
             d=d0+atan((b0/a0)*tan(d0))
             g=g+c*sin(d)
             d0=d+pi*int(d/pi+.5d0)
          endif
          a0=a
          b0=b
          if (c < 1.0d-7) exit
       end do
       ce=pi*(2.0d0-r)/(4.0d0*a)
       if (phi == pi2) then
          f = ce
       else
          ck=pi/(2.0d0*a)
          f =d/fac/a*ce/ck+g
       endif
    endif
  end function f_ielle



  recursive function f_iellf(x, phi) result(f)
    real(fdp), intent(in) :: x, phi
    real(fdp) :: f

    real(fdp) :: g, a, a0, b, b0, c, r, d, d0, ck, fac
    integer :: n

    if (phi == 0) then
       f = 0.0d0
    else if (x == 0.0d0) then
       f = x
    else if (x == 1.0d0 .and. phi == pi2) then
       f = inf
    else if (x == 1.0d0) then
       f = log(1.0d0+sin(phi))/cos(phi)
    else
       g=0.0d0
       a0=1.0d0
       b0=sqrt(1.0d0-x*x)
       d0 = phi
       r=x*x
       fac=1.0d0

       do n=1,40
          a=(a0+b0)/2.0d0
          b=sqrt(a0*b0)
          c=(a0-b0)/2.0d0
          fac=2.0d0*fac
          r=r+fac*c*c
          if (phi /= pi2) then
             d=d0+atan((b0/a0)*tan(d0))
             g=g+c*sin(d)
             d0=d+pi*int(d/pi+.5d0)
          endif
          a0=a
          b0=b
          if (c < 1.0d-7) exit
       end do

       if (phi == pi2) then
          ck=pi/(2.0d0*a)
          f=ck
       else
          f=d/(fac*a)
       endif
    endif
  end function f_iellf

